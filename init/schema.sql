-- Table for individuals
CREATE TABLE IF NOT EXISTS individual (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    monthly_income REAL,
    age INTEGER,
    full_name TEXT,
    phone TEXT,
    email TEXT,
    category TEXT,
    balance REAL
);

-- Table for companies
CREATE TABLE IF NOT EXISTS company (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    revenue REAL,
    age INTEGER,
    trade_name TEXT,
    phone TEXT,
    corporate_email TEXT,
    category TEXT,
    balance REAL
);

